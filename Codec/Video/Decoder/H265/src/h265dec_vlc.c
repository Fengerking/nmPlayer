/*!
 *************************************************************************************
 * \brief
 *    ReadUEV, reads an se(v) syntax element, the length in bits is stored in
 *    the global p_Dec->UsedBits variable
 *
 * \param tracestring
 *    the string for the trace file
 *
 * \param bitstream
 *    the stream to be read from
 *
 * \return
 *    the value of the coded syntax element
 *
 *************************************************************************************
 */
VO_S32 ReadSEV (BIT_STREAM *pBits)
{
	VO_U32 value;
	VO_S32 len=0;
	value = SHOW_BITS(pBits,32);
	if (value >= (1 << 27)) {
		value >>= 32-9;
		FLUSH_BITS(pBits,VO_VLC_LEN[value]);
		UPDATE_CACHE(pBits);
		return VO_SE_VAL[value];
	} else {
		len = 2*log2_c(value)-31;
		value >>= len;
	
		FLUSH_BITS_LARGE(pBits,32-len);
		UPDATE_CACHE(pBits);
		if (value&1)
			value = 0-(value>>1);
		else
			value = (value>>1);

		return value;
	}
}

/*!
 *************************************************************************************
 * \brief
 *    ReadUEV, reads an u(v) syntax element, the length in bits is stored in
 *    the global p_Dec->UsedBits variable
 *
 * \param LenInBits
 *    length of the syntax element
 *
 * \param tracestring
 *    the string for the trace file
 *
 * \param bitstream
 *    the stream to be read from
 *
 * \return
 *    the value of the coded syntax element
 *
 *************************************************************************************
 */
VO_S32 ReadUV(BIT_STREAM *pBits, VO_S32 LenInBits)
{
	VO_S32 inf;
	inf = GetBits(pBits,LenInBits);
	UPDATE_CACHE(pBits);
	return inf;
}
/*!
 *************************************************************************************
 * \brief
 *    ReadSV, reads an i(v) syntax element, the length in bits is stored in
 *    the global p_Dec->UsedBits variable
 *
 * \param LenInBits
 *    length of the syntax element
 *
 * \param tracestring
 *    the string for the trace file
 *
 * \param bitstream
 *    the stream to be read from
 *
 * \return
 *    the value of the coded syntax element
 *
 *************************************************************************************
 */
VO_S32 ReadSV (BIT_STREAM *pBits, VO_S32 LenInBits)
{
	VO_S32 inf;
	inf = GetBits(pBits,LenInBits);
	UPDATE_CACHE(pBits);
	// can be negative
	inf = -( inf & (1 << (LenInBits - 1)) ) | inf;
	return inf;
}


