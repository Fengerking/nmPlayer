void voAMRNBDecSetZero (
    Word16 x[],         /* (o)    : vector to clear     */
    Word16 L            /* (i)    : length of vector    */
)
{
   Word32 num = (Word32)L;
   do{
	   *x++ = 0;
   }while(--num !=0);
}
